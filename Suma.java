public class Suma {
    public static void main(String[] args) {
        int numero1 = 5; // Primer número
        int numero2 = 10; // Segundo número

        int suma = numero1 + numero2; // Sumar los dos números

        System.out.println("La suma de " + numero1 + " y " + numero2 + " es: " + suma);
    }
}